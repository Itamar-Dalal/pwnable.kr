def brainfuck_interpreter(code, memory_size=30000):
    memory = [0] * memory_size
    pointer = 0
    code_ptr = 0
    code_len = len(code)
    
    # Precompute jump map for loops
    jump_forward = {}
    jump_backward = {}
    stack = []
    for i, c in enumerate(code):
        if c == '[':
            stack.append(i)
        elif c == ']':
            start = stack.pop()
            jump_forward[start] = i
            jump_backward[i] = start
    
    while code_ptr < code_len:
        cmd = code[code_ptr]
        if cmd == '>':
            pointer += 1
            if pointer >= memory_size:
                raise IndexError("Pointer moved out of memory bounds.")
        elif cmd == '<':
            pointer -= 1
            if pointer < 0:
                raise IndexError("Pointer moved out of memory bounds.")
        elif cmd == '+':
            memory[pointer] = (memory[pointer] + 1) % 256
        elif cmd == '-':
            memory[pointer] = (memory[pointer] - 1) % 256
        elif cmd == '.':
            print(chr(memory[pointer]), end='')
        elif cmd == ',':
            # Input not implemented in this demo
            memory[pointer] = 0
        elif cmd == '[':
            if memory[pointer] == 0:
                code_ptr = jump_forward[code_ptr]
        elif cmd == ']':
            if memory[pointer] != 0:
                code_ptr = jump_backward[code_ptr]
        code_ptr += 1
    
    # Print memory dump of all non-zero cells
    print("\nMemory after execution (non-zero cells):")
    for i, val in enumerate(memory):
        if val != 0:
            print(f"Cell[{i:04d}]: {val:02X}")

